# Hacer válidas las variables
Numero_1 = 10
Numero_2 = 5
ResultadoSuma = Numero_1 + Numero_2

# Función para la multiplicación
def Producto(Entrada1, Entrada2):
    ResultadoProducto = Entrada1 * Entrada2
    return ResultadoProducto

ResultadoFinal = Producto(Numero_1, ResultadoSuma)
print(ResultadoFinal)